package backend.infrastructure.out.response;

public class ListSupplierResponse {
    private Long id;
    private String name;

    public ListSupplierResponse(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
